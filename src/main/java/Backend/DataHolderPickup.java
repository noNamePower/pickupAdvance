public class DataHolderPickup {
    int kc;
    int nc;
    int sets;
    int date;
    int lay;

    public void setKc(int kc) {
        this.kc = kc;
    }

    public int getKc() {
        return kc;
    }

    public void setNc(int nc) {
        this.nc = nc;
    }

    public int getNc() {
        return nc;
    }

    public void setSets(int sets) {
        this.sets = sets;
    }

    public int getSets() {
        return sets;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public int getDate() {
        return date;
    }

    public void setLay(int lay) {
        this.lay = lay;
    }

    public int getLay() {
        return lay;
    }
}
